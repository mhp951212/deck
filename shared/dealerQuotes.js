// 随机获取一条骚话
function getDealerQuote(actionType) {
  // actionType: 'raise' | 'allIn' | 'both'
  let pool;

  if (actionType === 'raise') {
    pool = [...dealerQuotes.raise, ...dealerQuotes.both.slice(0, 10)];
  } else if (actionType === 'allIn') {
    pool = [...dealerQuotes.allIn, ...dealerQuotes.both.slice(10)];
  } else {
    pool = dealerQuotes.both;
  }

  const randomIndex = Math.floor(Math.random() * pool.length);
  return pool[randomIndex];
}

