// 根据行动类型随机获取一句骚话
function getRandomQuote(actionType) {
  const pool = DEALER_QUOTES[actionType] || DEALER_QUOTES.general;
  // 也有一定概率从 general 中抽
  if (Math.random() < 0.15) {
    return DEALER_QUOTES.general[
      Math.floor(Math.random() * DEALER_QUOTES.general.length)
    ];
  }
  return pool[Math.floor(Math.random() * pool.length)];
}

